# list method

list1=[2, 3, 4, 1, 7, 5, 9, 0]

list1.append(10) # add 10 at the last
print(list1)

list1.reverse() # reverse the list
print(list1)

list1.sort() # ascending order sort
print(list1)

list1.sort(reverse=True) # descending order sort
print(list1)

list1.insert(0, 15) # in 0 index insert 15
print(list1)

list1.remove(4) # remove 1st 4 from the list
print(list1)

list1.pop(3) # remove the element in index 3
print(list1)

